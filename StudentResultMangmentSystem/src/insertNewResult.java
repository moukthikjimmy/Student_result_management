    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
         String rollno = jTextField1.getText();
        String bio = jTextField2.getText();
        String chem = jTextField3.getText();
        String mat = jTextField4.getText();
        String bet = jTextField5.getText();
        String psuc = jTextField6.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","mysql");
            Statement st = con.createStatement();
           ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
           if(rs.next()){
               st.executeUpdate("INSERT INTO result(rollno,biology,chemistry,mathematics, bet,psuc) VALUES ('"+rollno+"', '"+bio+"', '"+chem+"', '"+mat+"', '"+bet+"', '"+psuc+"')");
               JOptionPane.showMessageDialog(null, "Successfully updated!!");
               setVisible(false);
               new insertNewResult().setVisible(true);
               
               int s1 = Integer.parseInt(jTextField2.getText());
                int s2 = Integer.parseInt(jTextField3.getText());
                int s3 = Integer.parseInt(jTextField4.getText());
                int s4 = Integer.parseInt(jTextField5.getText());
                int s5 = Integer.parseInt(jTextField6.getText());
               String sw="";
                ResultSet rs1 = st.executeQuery("select * from result where rollno='"+rollno+"'");
                 if(s1<35 || s2<35 || s3<35 || s4<35 || s5<35){
                sw="dt";
                if(rs1.next()){
                st.executeUpdate("update result set result_status= 'DT' where rollno='"+rollno+"'" );
                }
                }
                else{
                sw="pass";
                if(rs1.next()){
                st.executeUpdate("update result set result_status= 'SAFE' where rollno='"+rollno+"'");
                }  
                }
           }else{
                  JOptionPane.showMessageDialog(null, "Roll Number does not exits in our database!");
           }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            setVisible(false);
            new insertNewResult().setVisible(true);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

