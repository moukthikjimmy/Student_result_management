    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
         String course = (String)jComboBox1.getSelectedItem();
        String gender = (String)jComboBox3.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        String name = jTextField2.getText();
        String DOB = jTextField3.getText();
        String rollno = jTextField1.getText();
        
                try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","mysql");
            Statement st = con.createStatement();
            st.executeUpdate("INSERT INTO student(course,branch,rollNo,name,gender,dob) VALUES ('"+course+"', '"+branch+"', '"+rollno+"', '"+name+"', '"+gender+"', '"+DOB+"')");
            JOptionPane.showMessageDialog(null, "Successfully Updated!!!");
            setVisible(false);
            new adminHome().setVisible(true);
            st.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            setVisible(false);
            new adminHome().setVisible(true);
        }

    }//GEN-LAST:event_jButton7ActionPerformed

